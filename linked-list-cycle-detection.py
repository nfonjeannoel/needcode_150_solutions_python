    node1.next = node2
    node2.next = node3
    node3.next = node4
    node4.next = node2  # Creates a cycle here

    solution = Solution()
    print(solution.hasCycle(node1))  # Output: True

    nodeA = ListNode(1)
    nodeB = ListNode(2)

    nodeA.next = nodeB
    nodeB.next = None  # No cycle

    print(solution.hasCycle(nodeA))  # Output: False


